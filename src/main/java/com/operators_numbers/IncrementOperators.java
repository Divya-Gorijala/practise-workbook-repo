package com.operators_numbers;

public class IncrementOperators {
    public static void main(String[] args) {
        int x=5;
        int y=8;
        int result=++x+y;
        System.out.println(result);

        x=5;
        result=x++ +y;
        System.out.println(result);
        System.out.println(4.0/0.0);
    }
}
